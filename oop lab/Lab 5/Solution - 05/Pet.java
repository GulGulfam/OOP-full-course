public class Pet{

	private String name;
	private String animal;
	private int age;

	public void setName(String n){
		name = n;
	}//setName()

	public void setAnimal(String a){
		animal = a;
	}//setAnimal()

	public void setAge(int a){
		age = a;
	}//setAge()

	public String getName(){
		return name;
	}//getName()

	public String getAnimal(){
		return animal;
	}//getAnimal()

	public int getAge(){
		return age;
	}//getAge()

}//class